function RC = RINT(YY, AL)
    % 高斯积分节点和权重
    A = [-0.989400934991649, -0.944575023073232, -0.865631202387831, ...
         -0.755404408355003, -0.617876244402643, -0.458016777657227, ...
         -0.281603550779258, -0.095012509837637,  0.989400934991649, ...
          0.944575023073232,  0.865631202387831,  0.755404408355003, ...
          0.617876244402643,  0.458016777657227,  0.281603550779258, ...
          0.095012509837637];
    W = [0.027152459411754, 0.062253523938647, 0.095158511682492, ...
         0.124628971255533, 0.149595988816576, 0.169156519395002, ...
         0.182603415044923, 0.189450610455068, 0.027152459411754, ...
         0.062253523938647, 0.095158511682492, 0.124628971255533, ...
         0.149595988816576, 0.169156519395002, 0.182603415044923, ...
         0.189450610455068];
    
    % 初始化复数矩阵
    RC = zeros(2, 2, 'like', complex(1.0, 0.0));
    PI = pi;
    
    % 处理输入参数
    if real(YY) < 0
        Y = -YY;
        SIG = -1;
    else
        Y = YY;
        SIG = 1;
    end
    
    YA = imag(Y);
    YR = real(Y);
    UL = PI - 2.8 * Y / (36 + Y);
    COT = cos(PI*Y) / sin(PI*Y);
    D = PI * (1 + COT^2);
    C = YR / AL;
    XO = log(C + sqrt(1 + C^2));
    
    % 高斯积分循环
    for I = 1:16
        X = UL/2 * (1 + A(I));
        Z = sin(X);
        C = cos(X);
        G = YR/AL * X/Z;
        T = sqrt(1 + G^2);
        B = log(G + T);
        G = (1/X - C/Z) * G / T;
        T = AL * (T*C - 1);
        Z = exp(X*YA);
        C = 0.5*(Z + 1/Z);
        S = 0.5i*(Z - 1/Z);
        F = COT + G;
        H = 1 - G*COT;
        EXF = exp(T - Y*B);
        O = B*C + X*S;
        P = X*C - B*S;
        XY = X*YR;
        R = (F*C + H*S) * EXF;
        RY = (F*O - H*P + D*(C - G*S)) * EXF;
        RP = ((F*T - H*XY)*C + (H*T + F*XY)*S) * EXF;
        RPY = (F*(T*O - XY*P) - H*(T*P + XY*O) + ( (T + XY*G)*C - (G*T - XY)*S )*D ) * EXF;
        
        X = XO/2 * (1 + A(I));
        Z = exp(X);
        C = (Z + 1/Z)/2 - 1;
        P = exp(AL*C - Y*X);
        RC(1,1) = RC(1,1) + W(I)*(UL*R + XO*P);
        RC(2,1) = RC(2,1) - W(I)*(UL*RY + XO*X*P);
        RC(1,2) = RC(1,2) + W(I)*(UL*RP + XO*AL*C*P);
        RC(2,2) = RC(2,2) - W(I)*(UL*RPY + XO*AL*X*C*P);
    end
    
    % 后处理
    O = Y/AL;
    P = Y^2 / 2;
    RC(1,1) = O*(Y*RC(1,1)/2 - 1) * SIG;
    RC(2,1) = 2*RC(1,1) + O*(P*RC(2,1) + 1) * SIG;
    RC(1,2) = Y*O*RC(1,2)/2 * SIG;
    RC(2,2) = 2*RC(1,2) + O*P*RC(2,2) * SIG;
end